-- =============================================
-- 删除用户数据管理菜单
-- =============================================
-- 如果之前已经运行过包含用户数据菜单的脚本，请运行此脚本清理
-- =============================================

USE ry;

-- 删除用户数据菜单项（menu_id = 2007）
DELETE FROM sys_menu WHERE menu_id = 2007;

-- 删除相关的角色权限关联
DELETE FROM sys_role_menu WHERE menu_id = 2007;

COMMIT;

SELECT '✓ 用户数据管理菜单已删除' AS status;
SELECT '请重新登录系统以刷新权限' AS next_step;












